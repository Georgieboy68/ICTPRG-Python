# print totals every number between 10 and 50, and outputs the subtotal on a separate line.
# initialise number to 10
number = 10
subtotal = 0
# while number <= 50
while number <= 50:
#     add number to previous subtotal
      subtotal = subtotal + number
#     print subtotal in a line
      print(subtotal)
#     add 1 to number
      number = number + 1    
# end 